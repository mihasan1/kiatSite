<p>&nbsp; </p>
<p align="justify">&nbsp;&nbsp;&nbsp;Київський авіаційний технікум утворений згідно наказу Міністерства авіаційної промисловості СРСР від 03.10.1966 р. № 566 для підготовки фахівців для авіаційної промисловості. </p>
<p align="justify">&nbsp;&nbsp;&nbsp;Технікум заснований на державній формі власності. </p>
<p align="justify">&nbsp;&nbsp;&nbsp;Підготовка спеціалістів з дня заснування технікуму велася за фахом 0511 «Літакобудування». </p>
<p align="justify">&nbsp;&nbsp;&nbsp;Відповідно до «Основних напрямків перебудови вищої і середньої спеціальної освіти в країні» на підставі наказу № 810 від 27.11.1987 р. про новий перелік спеціальностей з 1989 р. навчання ведеться за фахом 5.10010101 «Виробництво авіаційних суден» та за фахом «Економіка підприємства». </p>
<p align="justify">&nbsp;&nbsp;&nbsp;Відповідно до Указу Президії Верховної Ради України від 30.08.1991 р. «Про передання підприємств, установ та організацій союзного підпорядкування, розташованих на території України у державну власність» та дорученням Кабінету Міністрів України від 20.09.1991 р. №18602/18 Київський авіаційний технікум був переданий з 01.10.1991 р. у підпорядкування Міністерству освіти України. </p>
