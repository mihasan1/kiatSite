                <p align="center"><strong>Процедура проходження тестування. </strong></p>
                <p align="justify"> &nbsp;&nbsp;&nbsp;До пунктів тестування потрібно прийти о 10:30, з собою потрібно мати запрошення і паспорт. За відсутністю паспорта ви маєте право пред’явити свідотство про народження разом із довідкою з школи, з кольоровим фото, засвідченою підписом директора та печаткою. В холі пункту тестування будуть вивішені списки учасників за абеткою. Напроти прізвища стоїть номер аудиторії, в якій ви будете проходити тестування. Аудиторію можна знайти за вказівниками. На вході до аудиторії ви повинні показати інструктору свій паспорт та зайняти місце відповідно номера в протоколі проведення тестування. Також за відсутності паспорту ви може пред’явити свідотство про народження разом із довідкою з школи, з кольоровим фото, засвідченою підписом директора та печаткою. Під час тестування заборонено користуватися будь-якими матеріалами чи посібниками, а також – книжками, маркерами, плеярами, мобільними телефонами та будь-якими електронними та фотографічними засобами. Вам повинні зачитати правила проходження зовнішнього незалежного оцінювання. Згідно з правилами учасник що є третім за списком у протоколі тестування засвідчує неушкодженість тестового пакету ставлячи відповідні відмітки у протоколі. Після засвідчення неушкоджуваності пакету його відкривають та роздають кожному учневі бланки відповідей. На зворотній стороні бланку ви повинні своєю рукою зробити помітку «З правилами проходження зовнішнього незалежного оцінювання ознайомлений (-на)». Для кодування бланків інструктори використовують комплект наліпок з штрих кодами. Це робиться для забезпечення максимальної об ` єктивності перевірки тестування. Після цього інструктор видає кожному зошит. Номер тестового зошиту позначається в одній клітинці на бланках відповідей. Після початку тестування інструкторам не можна ставити запитання які стосуються змісту завдань, якщо виникла проблема то піднесіть руку і до вас підійде інструктор. Якщо під час тестування вам необхідно вийти – піднесіть руку, але врахуйте що час на виконання роботи не буде продовжено. Не забудьте залишити час на перенесення відповідей із тестових зошитів до бланків, як показує практика це займає близько десяти хвилин. Після завершення часу на виконання роботи потрібно здати бланки по порядку, начинаючи з першого номеру учасника, при цьому не забудьте розписатися в протоколі. Після цього учасники сідають на свої місця. Отримавши матеріали від останнього учня інструктори повинні підписати протокол проведення тестування. У присутності учнів вони повинні вкласти бланки тестування в порожній захисний пакет і надійно його заклеїти. Інформацію про результати проходження зовнішнього незалежного оцінювання можна отримати за місцем навчання починаючи з першого червня. </p>
