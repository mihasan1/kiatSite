                  <p align="justify">&nbsp;&nbsp;&nbsp;Державний вищий навчальний заклад І рівня акредитації, який здійснює освітню діяльність на основі
<a href="includes\statyt_kiat.pdf"><strong>Статуту Київського авіаційного технікуму</strong></a>, затверджений наказом від 03.10.2011 №1145, номер запису 10721050003006694, <a href="http://kiat.org.ua/sertifikat.php"><strong>ліцензія на надання освітніх послуг навчальними закладами</strong></a>, пов’язаних з одержанням вищої освіти на кваліфікаційних вимог до молодшого спеціаліста, серії АВ №458577, строк дії ліцензії 01.07.2019 р.</p>


              <p align="justify">&nbsp;&nbsp;&nbsp;Київський авіаційний технікум здійснюється підготовка фахівців за освітньо-кваліфікаційними рівнями молодшого 

спеціаліста (І-ІІ рівнів акредитації):</p>
              <p align="justify">&nbsp;&nbsp;&nbsp;Напрям 0511 Механічна інженерія. Спеціальність 5.05110101 Авіаційна та ракетно-космічна техніка. Спеціалізація: Виробництво авіаційних літальних апаратів. 

апаратів — сертифікат про акредитацію спеціальності 5.05110101 Авіаційна та ракетно-космічна техніка, серія НД-1 №1170100, термін дії сертифіката до 01.07.2019 

р.;</p>
              <p align="justify">&nbsp;&nbsp;&nbsp;Напрям 0305  Соціальні та поведінкові науки. Спеціальність 5.003050401 Економіка. Спеціалізація: Економіка підприємства.  сертифікат про акредитацію 

спеціальності 5.003050401 Економіка, серія НД-1 №1170099, термін дії сертифіката до 01.07.2019 р.;</p>
  
<p align="justify">&nbsp;&nbsp;&nbsp;Напрям 0501  Інформаційні технології. Спеціальність 5.05010301 Інженерія програмного забезпечення. Спеціалізація: Розробка програмного забезпечення термін дії ліцензії до 01.07.2018 

